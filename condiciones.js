const x=6
if (x<1) {
    console.log ("si es menor")
} else if (x==1){
    console.log ("es igual")
}else {
    console.log ("no aplica")
}




// terniario (otro if)
x<1 ? 
console.log ("si es menor")
:
console.log ("no es menor")





switch (x) {
    case x=1:
        console.log ("es igual")
        break;
    case x=4:
        console.log ("no es igual")
        break;
    default:
        console.log ("no aplica")
        break;
}